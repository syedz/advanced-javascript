"use strict";

/*
  What are the different types in javascript?

  Boolean // true/false
  Number // 1, 1.0
  String // "a", 'a',
  Null // null
  Undefined // undefined

  Object // {} or new Object()
 */

console.log(typeof(1));
console.log(typeof(null));
console.log(typeof(undefined));

/*
  Dynamically typed languages, variable types are declared at the time of execution,
  Statically typed languages have to declare the variable type.
 */

var a;
console.log(a);
